    private void button5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button5ActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql:///customerstuff","root","And98rew");
            String sql = "select * from book_info where book_id = ?";
        
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setInt(1, Integer.parseInt(txtBookID.getText()));
            
            ResultSet rs = pstmt.executeQuery();
            if (rs.next()) {
                txtBookTitle.setText(rs.getString("book_title"));
                txtBookAuthor.setText(rs.getString("book_author"));
                txtBookSubject.setText(rs.getString("subject"));
                txtQuantity.setText(rs.getString("quantity"));
            }
            else {
                JOptionPane.showMessageDialog(null, "Record not found");
            }
            
            conn.close();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_button5ActionPerformed


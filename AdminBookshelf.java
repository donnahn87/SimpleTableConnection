        // TODO add your handling code here: (Add Button)
        String bookTitle = txtBookTitle.getText();
        String author = txtBookAuthor.getText();
        String subject = txtBookSubject.getText();
        String quantity = txtQuantity.getText();
        
        Connection conn = null;
        PreparedStatement pstmt = null;

        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql:///customerstuff","root","And98rew");
            pstmt = conn.prepareStatement("insert into book_info values(?, ?, ?, ?, book_id)");
            pstmt.setString(1, bookTitle);
            pstmt.setString(2, author);
            pstmt.setString(3, subject);
            pstmt.setString(4, quantity);
            
            int i = pstmt.executeUpdate();
            if (i > 0) {
                JOptionPane.showMessageDialog(null, "Data saved");
            }
            else {
                JOptionPane.showMessageDialog(null, "Data not saved");
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
